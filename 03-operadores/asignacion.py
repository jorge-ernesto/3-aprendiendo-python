# OPERADORES DE ASIGNACION
edad = 55
print(edad)

edad += 5
print(edad)

# OPERADORES DE INCREMENTO Y DECREMENTO
year = 2021
print(year)

# Incremento
year = year + 1
print(year)

# Decremento
year = year - 1
print(year)

# Pre incremento
year = 1 + year
print(year)

# Pre decremento
year = 1 - year
print(year)

